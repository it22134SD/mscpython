"""
Examples of variable definitions
"""

x = 1           # integer
y = 1.1         # float
s = "Thomas"    # 1st String
S = "Kamalakis" # 2nd String
print('x =', x)
print('y =', y)
print('s =', s)
print('S =', S)

z = x + y
print('z =', z)

Z = s * 4
print('Z =', Z)

print('type of variable x:', type(x) )
print('type of variable y:', type(y) )

print('type of variable z:', type(z) )
print('type of variable Z:', type(Z) )
